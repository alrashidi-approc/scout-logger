Offset latLngToOffset(double lat, double lng, Size size) {
  final x = (lng + 180) / 360 * size.width;
  final y = (90 - lat) / 180 * size.height;
  return Offset(x.clamp(0, size.width), y.clamp(0, size.height));
}
